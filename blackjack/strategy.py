# Illustrious 18 — count-based deviations
# no surrender entries since that's removed
I18 = {
    (16, 10, False): (0,  'S'),
    (15, 10, False): (4,  'H'),   # would be surrender, now just hit
    (10, 10, False): (4,  'D'),
    (10, 11, False): (4,  'D'),
    (12,  3, False): (2,  'S'),
    (12,  2, False): (3,  'S'),
    (11, 11, False): (1,  'D'),
    (9,   2, False): (1,  'D'),
    (9,   7, False): (3,  'D'),
}

def basic_strategy(player_total:  int,
                   dealer_upcard: int,
                   is_soft:       bool,
                   can_double:    bool) -> str:
    table  = BASIC_STRATEGY['soft'] if is_soft else BASIC_STRATEGY['hard']
    action = table.get((player_total, dealer_upcard),
                       'S' if player_total >= 17 else 'H')
    if action == 'D' and not can_double:
        action = 'H'
    return action

def i18_deviation(player_total:  int,
                  dealer_upcard: int,
                  is_soft:       bool,
                  true_count:    float,
                  can_double:    bool) -> str | None:
    key = (player_total, dealer_upcard, is_soft)
    if key in I18:
        threshold, action = I18[key]
        if true_count >= threshold:
            if action == 'D' and not can_double:
                return None
            return action
    return None